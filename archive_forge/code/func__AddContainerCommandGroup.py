def _AddContainerCommandGroup(parser):
    """Add flags to update the command in the container declaration."""
    command_group = parser.add_mutually_exclusive_group()
    command_group.add_argument('--container-command', type=NonEmptyString('--container-command'), help='      Sets command in the declaration to the specified value.\n      Empty string is not allowed.\n\n      Cannot be used in the same command with `--clear-container-command`.\n      ')
    command_group.add_argument('--clear-container-command', action='store_true', default=None, help='      Removes command from container declaration.\n\n      Cannot be used in the same command with `--container-command`.\n      ')