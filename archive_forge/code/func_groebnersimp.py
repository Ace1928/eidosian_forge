def groebnersimp(ex, deep, **opts):

    def traverse(e):
        if e.is_Atom:
            return e
        args = [traverse(x) for x in e.args]
        if e.is_Function or e.is_Pow:
            args = [trigsimp_groebner(x, **opts) for x in args]
        return e.func(*args)
    if deep:
        ex = traverse(ex)
    return trigsimp_groebner(ex, **opts)