from mpmath import *
from mpmath import fp
def test_fp_gamma():
    assert ae(fp.gamma(1), 1.0)
    assert ae(fp.gamma(1.5), 0.886226925452758)
    assert ae(fp.gamma(10), 362880.0)
    assert ae(fp.gamma(-0.5), -3.544907701811032)
    assert ae(fp.gamma(-7.1), 0.0016478244570263334)
    assert ae(fp.gamma(12.3), 83385367.89997)
    assert ae(fp.gamma(2 + 0j), 1.0 + 0j)
    assert ae(fp.gamma(-2.5 + 0j), -0.9453087204829419 + 0j)
    assert ae(fp.gamma(3 + 4j), 0.0052255384713692146 - 0.1725470792943002j)
    assert ae(fp.gamma(-3 - 4j), 1.4609973058747756e-05 - 2.076073331150907e-05j)
    assert ae(fp.fac(0), 1.0)
    assert ae(fp.fac(1), 1.0)
    assert ae(fp.fac(20), 2.43290200817664e+18)
    assert ae(fp.fac(-3.5), -0.9453087204829419)
    assert ae(fp.fac(2 + 3j), -0.4401134076370017 - 0.06363724312631702j)
    assert ae(fp.loggamma(1.0), 0.0)
    assert ae(fp.loggamma(2.0), 0.0)
    assert ae(fp.loggamma(3.0), 0.6931471805599453)
    assert ae(fp.loggamma(7.25), 7.0521854507385395)
    assert ae(fp.loggamma(1000.0), 5905.220423209181)
    assert ae(fp.loggamma(1e+50), 1.1412925464970229e+52)
    assert ae(fp.loggamma(1e+25 + 1e+25j), 5.6125802751733674e+26 + 5.7696599078528565e+26j)
    assert ae(fp.loggamma(3 + 4j), -1.7566267846037842 + 4.742664438034658j)
    assert ae(fp.loggamma(-0.5), 1.2655121234846454 - 3.141592653589793j)
    assert ae(fp.loggamma(-1.25), 1.3664317612369763 - 6.283185307179586j)
    assert ae(fp.loggamma(-2.75), 0.004487897535955773 - 9.42477796076938j)
    assert ae(fp.loggamma(-3.5), -1.309006684993042 - 12.566370614359172j)
    assert ae(fp.loggamma(-4.5), -2.813084081769316 - 15.707963267948966j)
    assert ae(fp.loggamma(-2 + 3j), -6.776523813485657 - 4.568791367260286j)
    assert ae(fp.loggamma(-1000.3), -5912.844034778521 - 3144.734246243383j)
    assert ae(fp.loggamma(-100 - 100j), -632.3511766683314 - 158.37641469650353j)
    assert ae(fp.loggamma(1e-10), 23.025850929882736)
    assert ae(fp.loggamma(-1e-10), 23.025850929998178 - 3.141592653589793j)
    assert ae(fp.loggamma(1e-10j), 23.025850929940457 - 1.5707963268526182j)
    assert ae(fp.loggamma(1e-10j - 1e-10), 22.679277339718205 - 2.3561944902500667j)