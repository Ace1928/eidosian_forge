import math
import pytest
from mpmath import *
def test_hyper_3f2_etc():
    assert hyper([1, 2, 3], [1.5, 8], -1).ae(0.6710899235153334)
    assert hyper([1, 2, 3, 4], [5, 6, 7], -1).ae(0.9023298803542551)
    assert hyper([1, 2, 3], [1.25, 5], 1).ae(28.924181329701906)
    assert hyper([1, 2, 3, 4], [5, 6, 7], 5).ae(1.519230734400665 - 1.1529845225075537j)
    assert hyper([1, 2, 3, 4, 5], [6, 7, 8, 9], -1).ae(0.9628875946288236)
    assert hyper([1, 2, 3, 4, 5], [6, 7, 8, 9], 1).ae(1.0428697385885857)
    assert hyper([1, 2, 3, 4, 5], [6, 7, 8, 9], 5).ae(1.3398065363107476 - 0.07143405251029226j)
    assert hyper([1, 2.79, 3.08, 4.37], [5.2, 6.1, 7.3], 5).ae(1.0996321464692607 - 1.7748052293979986j)
    assert hyper([1, 1, 1], [1, 2], 1) == inf
    assert hyper([1, 1, 1], [2, (101, 100)], 1).ae(100.01621213528313)