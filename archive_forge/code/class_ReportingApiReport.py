@dataclass
class ReportingApiReport:
    """
    An object representing a report generated by the Reporting API.
    """
    id_: ReportId
    initiator_url: str
    destination: str
    type_: str
    timestamp: network.TimeSinceEpoch
    depth: int
    completed_attempts: int
    body: dict
    status: ReportStatus

    def to_json(self):
        json = dict()
        json['id'] = self.id_.to_json()
        json['initiatorUrl'] = self.initiator_url
        json['destination'] = self.destination
        json['type'] = self.type_
        json['timestamp'] = self.timestamp.to_json()
        json['depth'] = self.depth
        json['completedAttempts'] = self.completed_attempts
        json['body'] = self.body
        json['status'] = self.status.to_json()
        return json

    @classmethod
    def from_json(cls, json):
        return cls(id_=ReportId.from_json(json['id']), initiator_url=str(json['initiatorUrl']), destination=str(json['destination']), type_=str(json['type']), timestamp=network.TimeSinceEpoch.from_json(json['timestamp']), depth=int(json['depth']), completed_attempts=int(json['completedAttempts']), body=dict(json['body']), status=ReportStatus.from_json(json['status']))