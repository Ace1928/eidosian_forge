def b62_decode(s):
    if s == '0':
        return 0
    sign = 1
    if s[0] == '-':
        s = s[1:]
        sign = -1
    decoded = 0
    for digit in s:
        decoded = decoded * 62 + BASE62_ALPHABET.index(digit)
    return sign * decoded