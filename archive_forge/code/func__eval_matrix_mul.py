def _eval_matrix_mul(self, other):

    def entry(i, j):
        vec = [self[i, k] * other[k, j] for k in range(self.cols)]
        try:
            return Add(*vec)
        except (TypeError, SympifyError):
            return reduce(lambda a, b: a + b, vec)
    return self._new(self.rows, other.cols, entry)