def _unblockify_then_matricize(self, vec):
    """Flatten the block dimensions then reshape to a batch matrix."""
    vec_flat = self._unblockify(vec)
    matrix = distribution_util.rotate_transpose(vec_flat, shift=-1)
    return matrix