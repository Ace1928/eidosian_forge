def named_nodes(self, *names):
    """Get the set of "op" nodes with the given name."""
    named_nodes = []
    for node in self._multi_graph.nodes():
        if isinstance(node, DAGOpNode) and node.op.name in names:
            named_nodes.append(node)
    return named_nodes