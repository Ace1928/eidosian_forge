def _unwrap_hash(self, hash):
    """given hash belonging to wrapper, return orig version"""
    prefix = self.prefix
    if not hash.startswith(prefix):
        raise exc.InvalidHashError(self)
    return self.orig_prefix + hash[len(prefix):]