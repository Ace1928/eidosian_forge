from mpmath import *
from mpmath import fp
def test_fp_zeta():
    assert ae(fp.zeta(1e+100), 1.0)
    assert ae(fp.zeta(3), 1.2020569031595942)
    assert ae(fp.zeta(2 + 0j), 1.6449340668482264 + 0j)
    assert ae(fp.zeta(0.93), -13.713619351638165)
    assert ae(fp.zeta(1.74), 1.9796863545771775)
    assert ae(fp.zeta(0.0), -0.5)
    assert ae(fp.zeta(-1.0), -0.08333333333333333)
    assert ae(fp.zeta(-2.0), 0.0)
    assert ae(fp.zeta(-3.0), 0.008333333333333333)
    assert ae(fp.zeta(-500.0), 0.0)
    assert ae(fp.zeta(-7.4), 0.0036537321227995885)
    assert ae(fp.zeta(2.1), 1.560216533503362)
    assert ae(fp.zeta(26.9), 1.000000007985481)
    assert ae(fp.zeta(26), 1.0000000149015549)
    assert ae(fp.zeta(27), 1.0000000074507118)
    assert ae(fp.zeta(28), 1.000000003725334)
    assert ae(fp.zeta(27.1), 1.000000006951755)
    assert ae(fp.zeta(32.7), 1.0000000001433242)
    assert ae(fp.zeta(100), 1.0)
    assert ae(fp.altzeta(3.5), 0.9275535777739481)
    assert ae(fp.altzeta(1), 0.6931471805599453)
    assert ae(fp.altzeta(2), 0.8224670334241132)
    assert ae(fp.altzeta(0), 0.5)
    assert ae(fp.zeta(-2 + 3j, 1), 0.13297115587929864 + 0.12305330040458777j)
    assert ae(fp.zeta(-2 + 3j, 5), 18.384866151867577 - 11.37701511059771j)
    assert ae(fp.zeta(1.0000000001), 9999999173.173574)
    assert ae(fp.zeta(0.9999999999), -9999999172.019142)
    assert ae(fp.zeta(1 + 1e-09j), 0.5772156649015329 - 999999999.9999999j)
    assert ae(fp.primezeta(2.5 + 4j), -0.16922458243438032 - 0.010847965298387727j)
    assert ae(fp.primezeta(4), 0.07699313976424685)
    assert ae(fp.riemannr(3.7), 2.3034079839110855)
    assert ae(fp.riemannr(8), 3.9011860449341498)
    assert ae(fp.riemannr(3 + 4j), 2.236965331425999 + 1.6339943856990282j)