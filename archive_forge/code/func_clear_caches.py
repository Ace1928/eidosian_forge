def clear_caches(self):
    """
        Clears caches generated by panel.io.cache function.
        """
    for cache in self._memoize_cache.values():
        cache.clear()
        if hasattr(cache, 'directory'):
            cache.cache.close()
            try:
                shutil.rmtree(cache.directory)
            except OSError:
                pass
    self._memoize_cache.clear()