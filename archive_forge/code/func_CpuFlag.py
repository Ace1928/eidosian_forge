def CpuFlag(managed_only=False):
    """Create the --cpu flag."""
    help_msg = 'Set a CPU limit in Kubernetes cpu units.\n\nCloud Run (fully managed) supports values 1, 2 and 4.  For Cloud Run (fully managed), 4 cpus also requires a minimum 2Gi `--memory` value.  Examples 2, 2.0, 2000m'
    if not managed_only:
        help_msg += '\n\nCloud Run for Anthos and Knative-compatible Kubernetes clusters support fractional values.  Examples .5, 500m, 2'
    return base.Argument('--cpu', help=help_msg)