from mpmath import *
from mpmath import fp
def test_fp_erf():
    assert fp.erf(2) == fp.erf(2.0) == fp.erf(2.0 + 0j)
    assert fp.erf(fp.inf) == 1.0
    assert fp.erf(fp.ninf) == -1.0
    assert ae(fp.erf(0), 0.0)
    assert ae(fp.erf(-0), -0.0)
    assert ae(fp.erf(0.3), 0.3286267594591274)
    assert ae(fp.erf(-0.3), -0.3286267594591274)
    assert ae(fp.erf(0.9), 0.7969082124228322)
    assert ae(fp.erf(-0.9), -0.7969082124228322)
    assert ae(fp.erf(1.0), 0.8427007929497149)
    assert ae(fp.erf(-1.0), -0.8427007929497149)
    assert ae(fp.erf(1.1), 0.8802050695740817)
    assert ae(fp.erf(-1.1), -0.8802050695740817)
    assert ae(fp.erf(8.5), 1.0)
    assert ae(fp.erf(-8.5), -1.0)
    assert ae(fp.erf(9.1), 1.0)
    assert ae(fp.erf(-9.1), -1.0)
    assert ae(fp.erf(20.0), 1.0)
    assert ae(fp.erf(-20.0), -1.0)
    assert ae(fp.erf(10000.0), 1.0)
    assert ae(fp.erf(-10000.0), -1.0)
    assert ae(fp.erf(1e+50), 1.0)
    assert ae(fp.erf(-1e+50), -1.0)
    assert ae(fp.erf(1j), 1.6504257587975428j)
    assert ae(fp.erf(-1j), -1.6504257587975428j)
    assert ae(fp.erf(2 + 3j), -20.829461427614568 + 8.687318271470163j)
    assert ae(fp.erf(-(2 + 3j)), -(-20.829461427614568 + 8.687318271470163j))
    assert ae(fp.erf(8 + 9j), -1072004.252506205 + 364149.91954310256j)
    assert ae(fp.erf(-(8 + 9j)), -(-1072004.252506205 + 364149.91954310256j))
    assert fp.erfc(fp.inf) == 0.0
    assert fp.erfc(fp.ninf) == 2.0
    assert fp.erfc(0) == 1
    assert fp.erfc(-0.0) == 1
    assert fp.erfc(0 + 0j) == 1
    assert ae(fp.erfc(0.3), 0.6713732405408726)
    assert ae(fp.erfc(-0.3), 1.3286267594591274)
    assert ae(fp.erfc(0.9), 0.20309178757716786)
    assert ae(fp.erfc(-0.9), 1.7969082124228322)
    assert ae(fp.erfc(1.0), 0.15729920705028513)
    assert ae(fp.erfc(-1.0), 1.8427007929497148)
    assert ae(fp.erfc(1.1), 0.11979493042591827)
    assert ae(fp.erfc(-1.1), 1.8802050695740817)
    assert ae(fp.erfc(8.5), 2.7623240713337716e-33)
    assert ae(fp.erfc(-8.5), 2.0)
    assert ae(fp.erfc(9.1), 6.696900427988608e-38)
    assert ae(fp.erfc(-9.1), 2.0)
    assert ae(fp.erfc(20.0), 5.395865611607901e-176)
    assert ae(fp.erfc(-20.0), 2.0)
    assert ae(fp.erfc(10000.0), 0.0)
    assert ae(fp.erfc(-10000.0), 2.0)
    assert ae(fp.erfc(1e+50), 0.0)
    assert ae(fp.erfc(-1e+50), 2.0)
    assert ae(fp.erfc(1j), 1.0 - 1.6504257587975428j)
    assert ae(fp.erfc(-1j), 1.0 + 1.6504257587975428j)
    assert ae(fp.erfc(2 + 3j), 21.829461427614568 - 8.687318271470163j, 1e-13)
    assert ae(fp.erfc(-(2 + 3j)), -19.829461427614568 + 8.687318271470163j, 1e-13)
    assert ae(fp.erfc(8 + 9j), 1072005.252506205 - 364149.91954310256j)
    assert ae(fp.erfc(-(8 + 9j)), -1072003.252506205 + 364149.91954310256j)
    assert ae(fp.erfc(20 + 0j), 5.395865611607901e-176 + 0j)