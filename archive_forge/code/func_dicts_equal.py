def dicts_equal(dict1, dict2, abs_tol=0.0001):
    """Compares to dicts whose values may be floating point numbers."""
    if dict1.keys() != dict2.keys():
        return False
    for k, v in dict1.items():
        if isinstance(v, float) and isinstance(dict2[k], float) and math.isclose(v, dict2[k], abs_tol=abs_tol):
            continue
        if v != dict2[k]:
            return False
    return True