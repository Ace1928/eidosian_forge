import math
import pytest
from mpmath import *
def test_hyper_2f3():
    mp.dps = 15
    assert hyper([1, 2], [3, 4, 5], 6) == hyp2f3(1, 2, 3, 4, 5, 6)
    a1, a2, b1, b2, b3 = ((1, 10), (2, 3), (3, 10), 2, 1.0 / 16)
    assert hyp2f3(a1, a2, b1, b2, b3, 10).ae(128.9820716069866)
    assert hyp2f3(a1, a2, b1, b2, b3, 1000).ae(6.630963288313128e+25)
    assert hyp2f3(a1, a2, b1, b2, b3, 10000).ae(4.686363936271334e+84)
    assert hyp2f3(a1, a2, b1, b2, b3, 100000).ae(8.663245123610308e+271)
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 6).ae('2.0291718386574980641e865')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 7).ae('7.7639836665710030977e2742')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 8).ae('3.2537462584071268759e8681')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 20).ae('1.2966030542911614163e+8685889627')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 * j).ae(-18.551602185587548 - 13.348031097874113j)
    assert hyp2f3(a1, a2, b1, b2, b3, 100 * j).ae(78634.3591245045 + 74459.53594528197j)
    assert hyp2f3(a1, a2, b1, b2, b3, 1000 * j).ae(5.976825502765279e+17 - 6.5136194809352616e+16j)
    assert hyp2f3(a1, a2, b1, b2, b3, 10000 * j).ae(-1.1779696326238583e+59 + 1.2297607505213134e+59j)
    assert hyp2f3(a1, a2, b1, b2, b3, 100000 * j).ae(2.984422896980438e+191 + 7.558716323149027e+190j)
    assert hyp2f3(a1, a2, b1, b2, b3, 1000000 * j).ae('7.4859161049322370311e+610 - 2.8467477015940090189e+610j')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 7 * j).ae('-1.7477645579418800826e+1938 - 1.7606522995808116405e+1938j')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 8 * j).ae('-1.6932731942958401784e+6137 - 2.4521909113114629368e+6137j')
    assert hyp2f3(a1, a2, b1, b2, b3, 10 ** 20 * j).ae('-2.0988815677627225449e+6141851451 + 5.7708223542739208681e+6141851452j')