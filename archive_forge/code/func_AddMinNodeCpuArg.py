def AddMinNodeCpuArg(parser, is_update=False):
    parser.add_argument('--min-node-cpu', help='      Minimum number of virtual CPUs this instance will consume when running on\n      a sole-tenant node.\n      ')
    if is_update:
        parser.add_argument('--clear-min-node-cpu', action='store_true', help='        Removes the min-node-cpu field from the instance. If specified, the\n        instance min-node-cpu will be cleared. The instance will not be\n        overcommitted and utilize the full CPU count assigned.\n        ')