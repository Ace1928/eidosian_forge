def _canGetWidth(self, thing):
    """Can we work out the width quickly?"""
    if isinstance(thing, (list, tuple)):
        for elem in thing:
            if not self._canGetWidth(elem):
                return 0
        return 1
    elif isinstance(thing, Flowable):
        return thing._fixedWidth
    else:
        return 1