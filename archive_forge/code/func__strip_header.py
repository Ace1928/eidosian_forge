import os
from ..interfaces import matlab
def _strip_header(doc):
    """Strip Matlab header and splash info off doc.

    Searches for the tag 'NIPYPE' in the doc and returns everything after that.

    """
    hdr = 'NIPYPE'
    cruft = '\x1b'
    try:
        index = doc.index(hdr)
    except ValueError as e:
        raise IOError('This docstring was not generated by Nipype!\n') from e
    index += len(hdr)
    index += 1
    doc = doc[index:]
    try:
        index = doc.index(cruft)
    except ValueError:
        index = len(doc)
    return doc[:index]