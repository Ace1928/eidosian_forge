import math
import pytest
from mpmath import *
def test_hankel():
    mp.dps = 15
    assert hankel1(0, 0.5).ae(0.9384698072408129 - 0.44451873350670656j)
    assert hankel1(1, 0.5).ae(0.2422684576748739 - 1.471472392670243j)
    assert hankel1(-1, 0.5).ae(-0.2422684576748739 + 1.471472392670243j)
    assert hankel1(1.5, 0.5).ae(0.0917016996256513 - 2.521465550421338j)
    assert hankel1(1.5, 3 + 4j).ae(0.006680686647672816 - 0.003668423161083913j)
    assert hankel2(0, 0.5).ae(0.9384698072408129 + 0.44451873350670656j)
    assert hankel2(1, 0.5).ae(0.2422684576748739 + 1.471472392670243j)
    assert hankel2(-1, 0.5).ae(-0.2422684576748739 - 1.471472392670243j)
    assert hankel2(1.5, 0.5).ae(0.0917016996256513 + 2.521465550421338j)
    assert hankel2(1.5, 3 + 4j).ae(14.783528526098568 - 7.397390270853447j)