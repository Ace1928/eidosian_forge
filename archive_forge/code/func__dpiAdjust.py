def _dpiAdjust(self):
    dpi = self._dpi
    if dpi:
        if dpi[0] != 72:
            self.imageWidth *= 72.0 / dpi[0]
        if dpi[1] != 72:
            self.imageHeight *= 72.0 / dpi[1]