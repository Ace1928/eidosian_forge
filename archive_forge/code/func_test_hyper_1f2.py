import math
import pytest
from mpmath import *
def test_hyper_1f2():
    mp.dps = 15
    assert hyper([1], [2, 3], 4) == hyp1f2(1, 2, 3, 4)
    a1, b1, b2 = ((1, 10), (2, 3), 1.0 / 16)
    assert hyp1f2(a1, b1, b2, 10).ae(298.74827255545574)
    assert hyp1f2(a1, b1, b2, 100).ae(224128961.48602948)
    assert hyp1f2(a1, b1, b2, 1000).ae(1.1669528298622675e+27)
    assert hyp1f2(a1, b1, b2, 10000).ae(2.4780514622487212e+86)
    assert hyp1f2(a1, b1, b2, 100000).ae(1.3885391458871524e+274)
    assert hyp1f2(a1, b1, b2, 1000000).ae('9.8851796978960318255e+867')
    assert hyp1f2(a1, b1, b2, 10 ** 7).ae('1.1505659189516303646e+2746')
    assert hyp1f2(a1, b1, b2, 10 ** 8).ae('1.4672005404314334081e+8685')
    assert hyp1f2(a1, b1, b2, 10 ** 20).ae('3.6888217332150976493e+8685889636')
    assert hyp1f2(a1, b1, b2, 10 * j).ae(-16.163252524618574 - 44.32156789648018j)
    assert hyp1f2(a1, b1, b2, 100 * j).ae(61938.15529451785 + 637349.4521594235j)
    assert hyp1f2(a1, b1, b2, 1000 * j).ae(8.455057657257696e+18 + 6.261969266997572e+18j)
    assert hyp1f2(a1, b1, b2, 10000 * j).ae(-8.977121118400859e+60 + 4.655052811173163e+59j)
    assert hyp1f2(a1, b1, b2, 100000 * j).ae(2.6398091437239323e+193 + 4.165808066687062e+193j)
    assert hyp1f2(a1, b1, b2, 1000000 * j).ae('3.5999042951925965458e+613 + 1.5026014707128947992e+613j')
    assert hyp1f2(a1, b1, b2, 10 ** 7 * j).ae('-8.3208715051623234801e+1939 - 3.6752883490851869429e+1941j')
    assert hyp1f2(a1, b1, b2, 10 ** 8 * j).ae('2.0724195707891484454e+6140 - 1.3276619482724266387e+6141j')
    assert hyp1f2(a1, b1, b2, 10 ** 20 * j).ae('-1.1734497974795488504e+6141851462 + 1.1498106965385471542e+6141851462j')