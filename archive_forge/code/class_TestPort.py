from openstack.network.v2 import port
from openstack.tests.unit import base
class TestPort(base.TestCase):

    def test_basic(self):
        sot = port.Port()
        self.assertEqual('port', sot.resource_key)
        self.assertEqual('ports', sot.resources_key)
        self.assertEqual('/ports', sot.base_path)
        self.assertTrue(sot.allow_create)
        self.assertTrue(sot.allow_fetch)
        self.assertTrue(sot.allow_commit)
        self.assertTrue(sot.allow_delete)
        self.assertTrue(sot.allow_list)
        self.assertDictEqual({'binding:host_id': 'binding:host_id', 'binding:profile': 'binding:profile', 'binding:vif_details': 'binding:vif_details', 'binding:vif_type': 'binding:vif_type', 'binding:vnic_type': 'binding:vnic_type', 'description': 'description', 'device_id': 'device_id', 'device_owner': 'device_owner', 'fields': 'fields', 'fixed_ips': 'fixed_ips', 'id': 'id', 'ip_address': 'ip_address', 'mac_address': 'mac_address', 'name': 'name', 'network_id': 'network_id', 'security_groups': 'security_groups', 'status': 'status', 'subnet_id': 'subnet_id', 'is_admin_state_up': 'admin_state_up', 'is_port_security_enabled': 'port_security_enabled', 'project_id': 'project_id', 'security_group_ids': 'security_groups', 'limit': 'limit', 'marker': 'marker', 'any_tags': 'tags-any', 'not_any_tags': 'not-tags-any', 'not_tags': 'not-tags', 'tags': 'tags', 'sort_dir': 'sort_dir', 'sort_key': 'sort_key'}, sot._query_mapping._mapping)

