def final_is_versioned(self, trans_id):
    if trans_id in self._versioned:
        return True
    if trans_id in self._removed_id:
        return False
    orig_path = self.tree_path(trans_id)
    if orig_path is None:
        return False
    return self._tree.is_versioned(orig_path)