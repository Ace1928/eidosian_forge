from mpmath import *
def test_interval_complex_arg():
    mp.dps = 15
    iv.dps = 15
    assert iv.arg(3) == 0
    assert iv.arg(0) == 0
    assert iv.arg([0, 3]) == 0
    assert iv.arg(-3).ae(pi)
    assert iv.arg(2 + 3j).ae(iv.arg(2 + 3j))
    z = iv.mpc([-2, -1], [3, 4])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(-1 + 4j))
    assert t.b.ae(mp.arg(-2 + 3j))
    z = iv.mpc([-2, 1], [3, 4])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(1 + 3j))
    assert t.b.ae(mp.arg(-2 + 3j))
    z = iv.mpc([1, 2], [3, 4])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(2 + 3j))
    assert t.b.ae(mp.arg(1 + 4j))
    z = iv.mpc([1, 2], [-2, 3])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(1 - 2j))
    assert t.b.ae(mp.arg(1 + 3j))
    z = iv.mpc([1, 2], [-4, -3])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(1 - 4j))
    assert t.b.ae(mp.arg(2 - 3j))
    z = iv.mpc([-1, 2], [-4, -3])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(-1 - 3j))
    assert t.b.ae(mp.arg(2 - 3j))
    z = iv.mpc([-2, -1], [-4, -3])
    t = iv.arg(z)
    assert t.a.ae(mp.arg(-2 - 3j))
    assert t.b.ae(mp.arg(-1 - 4j))
    z = iv.mpc([-2, -1], [-3, 3])
    t = iv.arg(z)
    assert t.a.ae(-mp.pi)
    assert t.b.ae(mp.pi)
    z = iv.mpc([-2, 2], [-3, 3])
    t = iv.arg(z)
    assert t.a.ae(-mp.pi)
    assert t.b.ae(mp.pi)