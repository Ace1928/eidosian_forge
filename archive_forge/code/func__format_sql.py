def _format_sql(cursor, sql):
    real_sql = None
    try:
        if hasattr(cursor, 'mogrify'):
            real_sql = cursor.mogrify(sql)
            if isinstance(real_sql, bytes):
                real_sql = real_sql.decode(cursor.connection.encoding)
    except Exception:
        real_sql = None
    return real_sql or to_string(sql)