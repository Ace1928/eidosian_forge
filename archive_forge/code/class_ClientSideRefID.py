class ClientSideRefID:
    """An ID generated by the client for objects not yet given an ObjectRef"""

    def __init__(self, id: bytes):
        assert len(id) != 0
        self.id = id

    @staticmethod
    def generate_id() -> 'ClientSideRefID':
        tid = uuid.uuid4()
        return ClientSideRefID(b'\xcc' + tid.bytes)