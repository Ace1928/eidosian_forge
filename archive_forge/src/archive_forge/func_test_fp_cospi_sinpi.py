from mpmath import *
from mpmath import fp
def test_fp_cospi_sinpi():
    assert ae(fp.sinpi(0), 0.0)
    assert ae(fp.sinpi(0.25), 0.7071067811865476)
    assert ae(fp.sinpi(0.5), 1.0)
    assert ae(fp.sinpi(0.75), 0.7071067811865476)
    assert ae(fp.sinpi(1), 0.0)
    assert ae(fp.sinpi(1.25), -0.7071067811865476)
    assert ae(fp.sinpi(1.5), -1.0)
    assert ae(fp.sinpi(1.75), -0.7071067811865476)
    assert ae(fp.sinpi(2), 0.0)
    assert ae(fp.sinpi(2.25), 0.7071067811865476)
    assert ae(fp.sinpi(0 + 3j), 0.0 + 6195.82386360859j)
    assert ae(fp.sinpi(0.25 + 3j), 4381.109126058245 + 4381.109068995069j)
    assert ae(fp.sinpi(0.5 + 3j), 6195.8239443081075 + 0j)
    assert ae(fp.sinpi(0.75 + 3j), 4381.109126058245 - 4381.109068995069j)
    assert ae(fp.sinpi(1 + 3j), 0.0 - 6195.82386360859j)
    assert ae(fp.sinpi(1.25 + 3j), -4381.109126058245 - 4381.109068995069j)
    assert ae(fp.sinpi(1.5 + 3j), -6195.8239443081075 + 0j)
    assert ae(fp.sinpi(1.75 + 3j), -4381.109126058245 + 4381.109068995069j)
    assert ae(fp.sinpi(2 + 3j), 0.0 + 6195.82386360859j)
    assert ae(fp.sinpi(2.25 + 3j), 4381.109126058245 + 4381.109068995069j)
    assert ae(fp.sinpi(-0.75), -0.7071067811865476)
    assert ae(fp.sinpi(-1e-10), -3.1415926535897934e-10)
    assert ae(fp.sinpi(1e-10), 3.1415926535897934e-10)
    assert ae(fp.sinpi(1e-10 + 1e-10j), 3.1415926535897934e-10 + 3.1415926535897934e-10j)
    assert ae(fp.sinpi(1e-10 - 1e-10j), 3.1415926535897934e-10 - 3.1415926535897934e-10j)
    assert ae(fp.sinpi(-1e-10 + 1e-10j), -3.1415926535897934e-10 + 3.1415926535897934e-10j)
    assert ae(fp.sinpi(-1e-10 - 1e-10j), -3.1415926535897934e-10 - 3.1415926535897934e-10j)
    assert ae(fp.cospi(0), 1.0)
    assert ae(fp.cospi(0.25), 0.7071067811865476)
    assert ae(fp.cospi(0.5), 0.0)
    assert ae(fp.cospi(0.75), -0.7071067811865476)
    assert ae(fp.cospi(1), -1.0)
    assert ae(fp.cospi(1.25), -0.7071067811865476)
    assert ae(fp.cospi(1.5), 0.0)
    assert ae(fp.cospi(1.75), 0.7071067811865476)
    assert ae(fp.cospi(2), 1.0)
    assert ae(fp.cospi(2.25), 0.7071067811865476)
    assert ae(fp.cospi(0 + 3j), 6195.8239443081075 + 0j)
    assert ae(fp.cospi(0.25 + 3j), 4381.109126058245 - 4381.109068995069j)
    assert ae(fp.cospi(0.5 + 3j), 0.0 - 6195.82386360859j)
    assert ae(fp.cospi(0.75 + 3j), -4381.109126058245 - 4381.109068995069j)
    assert ae(fp.cospi(1 + 3j), -6195.8239443081075 + 0j)
    assert ae(fp.cospi(1.25 + 3j), -4381.109126058245 + 4381.109068995069j)
    assert ae(fp.cospi(1.5 + 3j), 0.0 + 6195.82386360859j)
    assert ae(fp.cospi(1.75 + 3j), 4381.109126058245 + 4381.109068995069j)
    assert ae(fp.cospi(2 + 3j), 6195.8239443081075 + 0j)
    assert ae(fp.cospi(2.25 + 3j), 4381.109126058245 - 4381.109068995069j)
    assert ae(fp.cospi(-0.75), -0.7071067811865476)
    assert ae(fp.sinpi(-0.7), -0.8090169943749475)
    assert ae(fp.cospi(-0.7), -0.587785252292473)
    assert ae(fp.cospi(-3 + 2j), -267.74676148374823 + 0j)
    assert ae(fp.sinpi(-3 + 2j), 0.0 - 267.7448940410165j)
    assert ae(fp.sinpi(-0.7 + 2j), -216.61168022920793 - 157.37650009392036j)
    assert ae(fp.cospi(-0.7 + 2j), -157.37759774921756 + 216.610169436302j)