from __future__ import annotations
from typing import Iterable, Optional
from typing_extensions import Literal, Required, TypedDict
from .chat_completion_message_tool_call_param import ChatCompletionMessageToolCallParam
class ChatCompletionAssistantMessageParam(TypedDict, total=False):
    role: Required[Literal['assistant']]
    'The role of the messages author, in this case `assistant`.'
    content: Optional[str]
    'The contents of the assistant message.\n\n    Required unless `tool_calls` or `function_call` is specified.\n    '
    function_call: FunctionCall
    'Deprecated and replaced by `tool_calls`.\n\n    The name and arguments of a function that should be called, as generated by the\n    model.\n    '
    name: str
    'An optional name for the participant.\n\n    Provides the model information to differentiate between participants of the same\n    role.\n    '
    tool_calls: Iterable[ChatCompletionMessageToolCallParam]
    'The tool calls generated by the model, such as function calls.'