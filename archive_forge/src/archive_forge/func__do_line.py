def _do_line(tx, x1, y1, x2, y2, nlw, nsc):
    canv = tx._canvas
    olw = canv._lineWidth
    if nlw != olw:
        canv.setLineWidth(nlw)
    osc = canv._strokeColorObj
    if nsc != osc:
        canv.setStrokeColor(nsc)
    canv.line(x1, y1, x2, y2)