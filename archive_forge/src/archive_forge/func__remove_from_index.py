def _remove_from_index(obj, proc, index, remove_fun, callback=None):
    try:
        fd = obj.fileno()
    except OSError:
        return
    try:
        if index[fd] is proc:
            index.pop(fd, None)
    except KeyError:
        pass
    else:
        remove_fun(fd)
        if callback is not None:
            callback(fd)
    return fd