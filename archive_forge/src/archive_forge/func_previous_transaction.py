def previous_transaction(self):
    if self.previous_transaction_ is None:
        self.lazy_init_lock_.acquire()
        try:
            if self.previous_transaction_ is None:
                self.previous_transaction_ = Transaction()
        finally:
            self.lazy_init_lock_.release()
    return self.previous_transaction_