def is_valid_ospf_route_id(self):
    """check is ipv4 addr is valid"""
    if self.route_id.find('.') != -1:
        addr_list = self.route_id.split('.')
        if len(addr_list) != 4:
            return False
        for each_num in addr_list:
            if not each_num.isdigit():
                return False
            if int(each_num) > 255:
                return False
        return True
    return False