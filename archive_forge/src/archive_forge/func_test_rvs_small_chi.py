@pytest.mark.parametrize('chi', [1e-09, 1e-06])
def test_rvs_small_chi(self, chi):
    r = stats.argus.rvs(chi, size=500, random_state=890981)
    _, p = stats.kstest(r, lambda x: 1 - (1 - x ** 2) ** (3 / 2))
    assert_(p > 0.05)