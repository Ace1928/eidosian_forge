def _eval_rewrite_as_nonrepsmall(self, *args, **kwargs):
    x, n = self.args[-1].extract_branch_factor(allow_half=True)
    args = self.args[:-1] + (x,)
    if not n.is_Integer:
        return self._expr_small_minus(*args)
