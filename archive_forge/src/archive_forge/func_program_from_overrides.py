def program_from_overrides(self, command_names: T.List[mesonlib.FileOrString], extra_info: T.List['mlog.TV_Loggable']) -> T.Optional[T.Union[ExternalProgram, OverrideProgram, build.Executable]]:
    for name in command_names:
        if not isinstance(name, str):
            continue
        if name in self.build.find_overrides:
            exe = self.build.find_overrides[name]
            extra_info.append(mlog.blue('(overridden)'))
            return exe
    return None