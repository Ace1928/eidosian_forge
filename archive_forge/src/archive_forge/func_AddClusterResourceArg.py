def AddClusterResourceArg(parser: parser_arguments.ArgumentInterceptor, verb, positional=True, required=True, flag_name_overrides=None):
    """Adds a resource argument for an Anthos cluster on VMware.

  Args:
    parser: The argparse parser to add the resource arg to.
    verb: str, the verb to describe the resource, such as 'to update'.
    positional: bool, whether the argument is positional or not.
    required: bool, whether the argument is required or not.
    flag_name_overrides: {str: str}, dict of attribute names to the desired flag
      name.
  """
    name = 'cluster' if positional else '--cluster'
    concept_parsers.ConceptParser.ForResource(name, GetClusterResourceSpec(), 'cluster {}'.format(verb), required=required, flag_name_overrides=flag_name_overrides).AddToParser(parser)