@Boto3Mixin.aws_error_handler('list tags on resource')
def _describe_tags(self, id=None):
    if not id:
        id = self.resource_id
    filters = ansible_dict_to_boto3_filter_list({'resource-id': id})
    tags = self._paginated_describe_tags(Filters=filters)
    return tags