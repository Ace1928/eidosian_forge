from mpmath import *
from mpmath import fp
def test_fp_lambertw():
    assert ae(fp.lambertw(0.0), 0.0)
    assert ae(fp.lambertw(1.0), 0.5671432904097838)
    assert ae(fp.lambertw(7.5), 1.5662309537823875)
    assert ae(fp.lambertw(-0.25), -0.3574029561813889)
    assert ae(fp.lambertw(-10.0), 1.3699809685212707 + 2.1401945270747134j)
    assert ae(fp.lambertw(0 + 0j), 0.0 + 0j)
    assert ae(fp.lambertw(4 + 0j), 1.2021678731970429 + 0j)
    assert ae(fp.lambertw(1000.5), 5.250022745040898)
    assert ae(fp.lambertw(1e+100), 224.8431064451185)
    assert ae(fp.lambertw(-1000.0), 5.150163024636251 + 2.6641981432905206j)
    assert ae(fp.lambertw(1e-10), 9.999999999e-11)
    assert ae(fp.lambertw(1e-10j), 1.0000000000000001e-20 + 1e-10j)
    assert ae(fp.lambertw(3 + 4j), 1.281561806123776 + 0.533095222020971j)
    assert ae(fp.lambertw(-3 - 4j), 1.075073066569255 - 1.3251023817343588j)
    assert ae(fp.lambertw(10000 + 1000j), 7.23615265633716 + 0.08756781094383935j)
    assert ae(fp.lambertw(0.0, -1), -fp.inf)
    assert ae(fp.lambertw(1.0, -1), -1.5339133197935746 - 4.375185153061898j)
    assert ae(fp.lambertw(7.5, -1), 0.4412566841509861 - 4.803984200845239j)
    assert ae(fp.lambertw(-0.25, -1), -2.15329236411035)
    assert ae(fp.lambertw(-10.0, -1), 1.3699809685212707 - 2.1401945270747134j)
    assert ae(fp.lambertw(0 + 0j, -1), -fp.inf)
    assert ae(fp.lambertw(4 + 0j, -1), -0.15730793189620765 - 4.678780070466666j)
    assert ae(fp.lambertw(1000.5, -1), 4.915376541540402 - 5.446568270081516j)
    assert ae(fp.lambertw(1e+100, -1), 224.842721301016 - 6.255371383816724j)
    assert ae(fp.lambertw(-1000.0, -1), 5.150163024636251 - 2.6641981432905206j)
    assert ae(fp.lambertw(1e-10, -1), -26.303186778379043 - 3.2650939117038282j)
    assert ae(fp.lambertw(1e-10j, -1), -26.297238779529035 - 1.6328071613455766j)
    assert ae(fp.lambertw(3 + 4j, -1), 0.2585674068669974 - 3.8521166861614358j)
    assert ae(fp.lambertw(-3 - 4j, -1), -0.3202875020431077 - 6.880167719209197j)
    assert ae(fp.lambertw(10000 + 1000j, -1), 7.025530874228544 - 5.517750683573407j)
    assert ae(fp.lambertw(0.0, 2), -fp.inf)
    assert ae(fp.lambertw(1.0, 2), -2.401585104868003 + 10.77629951611507j)
    assert ae(fp.lambertw(7.5, 2), -0.3800335796284379 + 10.960916473368746j)
    assert ae(fp.lambertw(-0.25, 2), -4.055873526906151 + 13.85233465856727j)
    assert ae(fp.lambertw(-10.0, 2), -0.3447912376431886 + 14.112740596763592j)
    assert ae(fp.lambertw(0 + 0j, 2), -fp.inf)
    assert ae(fp.lambertw(4 + 0j, 2), -1.0070343323804263 + 10.903476551861683j)
    assert ae(fp.lambertw(1000.5, 2), 4.40761851654594 + 11.365524591091402j)
    assert ae(fp.lambertw(1e+100, 2), 224.84156762724876 + 12.510785262632256j)
    assert ae(fp.lambertw(-1000.0, 2), 4.198424561024653 + 14.420478573754314j)
    assert ae(fp.lambertw(1e-10, 2), -26.362258095445867 + 9.780024740703148j)
    assert ae(fp.lambertw(1e-10j, 2), -26.384250801683084 + 11.40353595060774j)
    assert ae(fp.lambertw(3 + 4j, 2), -0.8655467994333399 + 11.849956798331991j)
    assert ae(fp.lambertw(-3 - 4j, 2), -0.5579227387467911 + 8.717362702415933j)
    assert ae(fp.lambertw(10000 + 1000j, 2), 6.622380225458566 + 11.613486468250208j)