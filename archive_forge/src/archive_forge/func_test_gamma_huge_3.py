from mpmath import *
from mpmath.libmp import round_up, from_float, mpf_zeta_int
def test_gamma_huge_3():
    mp.dps = 500
    x = 10 ** 80 // 3 + 10 ** 70 * j / 7
    mp.dps = 15
    y = gamma(x)
    assert str(y.real) == '-6.82925203918106e+2636286142112569524501781477865238132302397236429627932441916056964386399485392600'
    assert str(y.imag) == '8.54647143678418e+2636286142112569524501781477865238132302397236429627932441916056964386399485392600'
    mp.dps = 50
    y = gamma(x)
    assert str(y.real) == '-6.8292520391810548460682736226799637356016538421817e+2636286142112569524501781477865238132302397236429627932441916056964386399485392600'
    assert str(y.imag) == '8.5464714367841748507479306948130687511711420234015e+2636286142112569524501781477865238132302397236429627932441916056964386399485392600'