class VHartreeCube(Section):
    """Controls printing of the hartree potential as a cube file."""

    def __init__(self, keywords: dict | None=None, subsections: dict | None=None, **kwargs):
        keywords = keywords or {}
        subsections = subsections or {}
        description = 'Controls the printing of a cube file with eletrostatic potential generated by the total density (electrons+ions). It is valid only for QS with GPW formalism. Note: by convention the potential has opposite sign than the expected physical one.'
        super().__init__('V_HARTREE_CUBE', subsections=subsections, description=description, keywords=keywords, **kwargs)