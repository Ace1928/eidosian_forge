def addLiteral(self, s, escaped=1):
    """introduce the literal text of PDF operations s into the current stream.
           Only use this if you are an expert in the PDF file format."""
    s = str(s)
    if escaped == 0:
        s = self._escape(s)
    self._code.append(s)