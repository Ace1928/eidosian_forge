def _generate_random_int(low: int=10, high: int=20, forbidden_values: Optional[List[int]]=None):
    if forbidden_values is None:
        forbidden_values = []
    value = random.randint(low, high)
    while value in forbidden_values:
        value = random.randint(low, high)
    return value