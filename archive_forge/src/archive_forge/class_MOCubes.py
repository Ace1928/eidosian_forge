class MOCubes(Section):
    """Controls printing of the molecular orbital eigenvalues."""

    def __init__(self, write_cube: bool=False, nhomo: int=1, nlumo: int=1, keywords: dict | None=None, subsections: dict | None=None, **kwargs):
        """Initialize the MO_CUBES section."""
        self.write_cube = write_cube
        self.nhomo = nhomo
        self.nlumo = nlumo
        keywords = keywords or {}
        subsections = subsections or {}
        description = 'Controls the printing of a cube file with eletrostatic potential generated by the total density (electrons+ions). It is valid only for QS with GPW formalism. Note: by convention the potential has opposite sign than the expected physical one.'
        _keywords = {'WRITE_CUBES': Keyword('WRITE_CUBE', write_cube), 'NHOMO': Keyword('NHOMO', nhomo), 'NLUMO': Keyword('NLUMO', nlumo)}
        keywords.update(_keywords)
        super().__init__('MO_CUBES', subsections={}, description=description, keywords=keywords, **kwargs)