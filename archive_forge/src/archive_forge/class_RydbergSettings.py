from dataclasses import dataclass
import numpy as np
import pennylane as qml
from pennylane.pulse import HardwareHamiltonian, HardwarePulse, drive
from pennylane.wires import Wires
from pennylane.pulse.hardware_hamiltonian import _reorder_parameters
@dataclass
class RydbergSettings:
    """Dataclass that contains the information of a Rydberg setup.

    Args:
        register (list): coordinates of atoms
        interaction_coeff (float): interaction coefficient C6 from C6/(Rij)**6 term in :func:`rydberg_interaction`
    """
    register: list
    interaction_coeff: float = 0.0

    def __eq__(self, other):
        return qml.math.all(self.register == other.register) and self.interaction_coeff == other.interaction_coeff

    def __add__(self, other):
        if other is not None:
            raise ValueError(f'Cannot add two `HardwareHamiltonian` instances with an interaction term. Obtained two instances with settings {self} and {other}. You most likely tried to add two terms generated by `qml.pulse.rydberg_interaction`')
        return self
    __radd__ = __add__