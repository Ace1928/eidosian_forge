def _add_global_arguments(self, node: mparser.FunctionNode, argsdict: T.Dict[str, T.List[str]], args: T.List[str], kwargs: 'kwtypes.FuncAddProjectArgs') -> None:
    if self.is_subproject():
        msg = f"Function '{node.func_name.value}' cannot be used in subprojects because there is no way to make that reliable.\nPlease only call this if is_subproject() returns false. Alternatively, define a variable that\ncontains your language-specific arguments and add it to the appropriate *_args kwarg in each target."
