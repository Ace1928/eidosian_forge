@classmethod
def _only_below_fermi(cls, i):
    """
        Tests if given orbit is only below fermi surface.

        If nothing can be concluded we return a conservative False.
        """
    if i.is_number:
        return i <= cls.fermi_level
    if i.assumptions0.get('below_fermi'):
        return True
    return False