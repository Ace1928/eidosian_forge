def _configure_polymorphic_setter(self, init=False):
    """Configure an attribute on the mapper representing the
        'polymorphic_on' column, if applicable, and not
        already generated by _configure_properties (which is typical).

        Also create a setter function which will assign this
        attribute to the value of the 'polymorphic_identity'
        upon instance construction, also if applicable.  This
        routine will run when an instance is created.

        """
    setter = False
    polymorphic_key: Optional[str] = None
    if self.polymorphic_on is not None:
        setter = True
        if isinstance(self.polymorphic_on, str):
            try:
                self.polymorphic_on = self._props[self.polymorphic_on]
            except KeyError as err:
                raise sa_exc.ArgumentError("Can't determine polymorphic_on value '%s' - no attribute is mapped to this name." % self.polymorphic_on) from err
        if self.polymorphic_on in self._columntoproperty:
            prop = self._columntoproperty[self.polymorphic_on]
        elif isinstance(self.polymorphic_on, MapperProperty):
            if not isinstance(self.polymorphic_on, properties.ColumnProperty):
                raise sa_exc.ArgumentError('Only direct column-mapped property or SQL expression can be passed for polymorphic_on')
            prop = self.polymorphic_on
        else:
            col = self.persist_selectable.corresponding_column(self.polymorphic_on)
            if col is None:
                setter = False
                instrument = False
                col = self.polymorphic_on
                if isinstance(col, schema.Column) and (self.with_polymorphic is None or self.with_polymorphic[1] is None or self.with_polymorphic[1].corresponding_column(col) is None):
                    raise sa_exc.InvalidRequestError("Could not map polymorphic_on column '%s' to the mapped table - polymorphic loads will not function properly" % col.description)
            else:
                instrument = True
            key = getattr(col, 'key', None)
            if key:
                if self._should_exclude(key, key, False, col):
                    raise sa_exc.InvalidRequestError('Cannot exclude or override the discriminator column %r' % key)
            else:
                self.polymorphic_on = col = col.label('_sa_polymorphic_on')
                key = col.key
            prop = properties.ColumnProperty(col, _instrument=instrument)
            self._configure_property(key, prop, init=init, setparent=True)
        self.polymorphic_on = prop.columns[0]
        polymorphic_key = prop.key
    else:
        for mapper in self.iterate_to_root():
            if mapper.polymorphic_on is not None:
                if self.persist_selectable is mapper.persist_selectable:
                    self.polymorphic_on = mapper.polymorphic_on
                else:
                    self.polymorphic_on = self.persist_selectable.corresponding_column(mapper.polymorphic_on)
                if self.polymorphic_on is not None:
                    self._set_polymorphic_identity = mapper._set_polymorphic_identity
                    self._polymorphic_attr_key = mapper._polymorphic_attr_key
                    self._validate_polymorphic_identity = mapper._validate_polymorphic_identity
                else:
                    self._set_polymorphic_identity = None
                    self._polymorphic_attr_key = None
                return
    if self.polymorphic_abstract and self.polymorphic_on is None:
        raise sa_exc.InvalidRequestError('The Mapper.polymorphic_abstract parameter may only be used on a mapper hierarchy which includes the Mapper.polymorphic_on parameter at the base of the hierarchy.')
    if setter:

        def _set_polymorphic_identity(state):
            dict_ = state.dict
            polymorphic_identity = state.manager.mapper.polymorphic_identity
            if polymorphic_identity is None and state.manager.mapper.polymorphic_abstract:
                raise sa_exc.InvalidRequestError(f"Can't instantiate class for {state.manager.mapper}; mapper is marked polymorphic_abstract=True")
            state.get_impl(polymorphic_key).set(state, dict_, polymorphic_identity, None)
        self._polymorphic_attr_key = polymorphic_key

        def _validate_polymorphic_identity(mapper, state, dict_):
            if polymorphic_key in dict_ and dict_[polymorphic_key] not in mapper._acceptable_polymorphic_identities:
                util.warn_limited('Flushing object %s with incompatible polymorphic identity %r; the object may not refresh and/or load correctly', (state_str(state), dict_[polymorphic_key]))
        self._set_polymorphic_identity = _set_polymorphic_identity
        self._validate_polymorphic_identity = _validate_polymorphic_identity
    else:
        self._polymorphic_attr_key = None
        self._set_polymorphic_identity = None