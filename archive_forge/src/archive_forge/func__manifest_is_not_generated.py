def _manifest_is_not_generated(self):
    if not os.path.isfile(self.manifest):
        return False
    fp = open(self.manifest)
    try:
        first_line = fp.readline()
    finally:
        fp.close()
    return first_line != '# file GENERATED by distutils, do NOT edit\n'