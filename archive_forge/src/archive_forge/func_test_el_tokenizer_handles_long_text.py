import pytest
def test_el_tokenizer_handles_long_text(el_tokenizer):
    text = 'Η Ελλάδα (παλαιότερα Ελλάς), επίσημα γνωστή ως Ελληνική Δημοκρατία,    είναι χώρα της νοτιοανατολικής Ευρώπης στο νοτιότερο άκρο της Βαλκανικής χερσονήσου.    Συνορεύει στα βορειοδυτικά με την Αλβανία, στα βόρεια με την πρώην    Γιουγκοσλαβική Δημοκρατία της Μακεδονίας και τη Βουλγαρία και στα βορειοανατολικά με την Τουρκία.'
    tokens = el_tokenizer(text)
    assert len(tokens) == 54