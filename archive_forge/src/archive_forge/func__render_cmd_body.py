def _render_cmd_body(op_container: ops.OpContainer, autogen_context: AutogenContext) -> str:
    buf = StringIO()
    printer = PythonPrinter(buf)
    printer.writeline('# ### commands auto generated by Alembic - please adjust! ###')
    has_lines = False
    for op in op_container.ops:
        lines = render_op(autogen_context, op)
        has_lines = has_lines or bool(lines)
        for line in lines:
            printer.writeline(line)
    if not has_lines:
        printer.writeline('pass')
    printer.writeline('# ### end Alembic commands ###')
    return buf.getvalue()