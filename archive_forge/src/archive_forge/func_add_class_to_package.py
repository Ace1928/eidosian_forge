def add_class_to_package(class_codes, class_names, module_name, package_dir):
    module_python_filename = os.path.join(package_dir, '%s.py' % module_name)
    f_m = open(module_python_filename, 'w')
    f_i = open(os.path.join(package_dir, '__init__.py'), 'a+')
    f_m.write('# -*- coding: utf-8 -*-\n"""Autogenerated file - DO NOT EDIT\nIf you spot a bug, please report it on the mailing list and/or change the generator."""\n\n')
    imports = 'from ..base import (CommandLine, CommandLineInputSpec, SEMLikeCommandLine, TraitedSpec,\n                    File, Directory, traits, isdefined, InputMultiPath, OutputMultiPath)\nimport os\n\n\n'
    f_m.write(imports)
    f_m.write('\n\n'.join(class_codes))
    f_i.write('from %s import %s\n' % (module_name, ', '.join(class_names)))
    f_m.close()
    f_i.close()